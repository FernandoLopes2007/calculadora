
float soma(float x, float y){
	float soma;
	soma = x+y;
	return soma;
}

float subtracao(float x, float y){
	float subtracao;
	subtracao = x-y;
	return subtracao;
}

float multiplicacao(float x, float y){
	float multiplicacao;
	multiplicacao = x*y;
	return multiplicacao;
}

float divisao(float x, float y){
	float divisao;
	divisao = x/y;
	return divisao;
}
